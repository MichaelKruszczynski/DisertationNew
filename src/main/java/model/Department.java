package model;

public class Department {
	
 private double hoursUsedForWeek;
 private String departmentName;
 private double hoursBooked;
 private String dayDue;
 
  public double getHoursUsedForWeek() {
	return hoursUsedForWeek;
  }
  public void setHoursUsedForWeek(double hoursUsedForWeek) {
	this.hoursUsedForWeek = hoursUsedForWeek;
  }
  public String getDepartmentName() {
	return departmentName;
  }
  public void setDepartmentName(String departmentName) {
	this.departmentName = departmentName;
  }
  public double getHoursBooked() {
	return hoursBooked;
  }
  public void setHoursBooked(double hoursBooked) {
	this.hoursBooked = hoursBooked;
  }
  public String getDayDue() {
	return dayDue;
  }
  public void setDayDue(String dayDue) {
	this.dayDue = dayDue;
}
 
  
  
  

}
